package com.sortinghat.funny.bean;

public class TaskCommonToastBean {

    private String pendantUrl;
    private String toast;
    private int starNote;
    private int awardStarNote;

    public String getPendantUrl() {
        return pendantUrl;
    }

    public void setPendantUrl(String pendantUrl) {
        this.pendantUrl = pendantUrl;
    }

    public String getToast() {
        return toast;
    }

    public void setToast(String toast) {
        this.toast = toast;
    }

    public int getStarNote() {
        return starNote;
    }

    public void setStarNote(int starNote) {
        this.starNote = starNote;
    }

    public int getAwardStarNote() {
        return awardStarNote;
    }

    public void setAwardStarNote(int awardStarNote) {
        this.awardStarNote = awardStarNote;
    }
}
